import importlib


def _dispatch(module_name: str, func_name: str = "cmd"):
    def _runner(args):
        module = importlib.import_module(module_name)
        getattr(module, func_name)(args)
    return _runner


def _add_io_args(p):
    p.add_argument("-i", "--input", required=True, help="Input file (TSV)")
    p.add_argument("-o", dest="output", help="Output file (default: stdout)")
    p.add_argument("--sep", default=None, help="Column separator (default: tab)")


def register_subparser(subparsers):
    math_parser = subparsers.add_parser("math", help="Mathematical and statistical tools")
    math_sub = math_parser.add_subparsers(dest="math_cmd")
    math_parser.set_defaults(_group_parser=math_parser)

    p = math_sub.add_parser("statistics", help="Descriptive statistics of a numeric column")
    _add_io_args(p)
    p.add_argument("-c", "--col", default="1", help="Column name or index (default: 1)")
    p.set_defaults(func=_dispatch("jsrc.math.statistics"))

    p = math_sub.add_parser("correlation", help="Pearson / Spearman correlation")
    _add_io_args(p)
    p.add_argument("-c", "--col", nargs=2, required=True, metavar=("COL1", "COL2"), help="Two column names or indices")
    p.add_argument("-m", "--method", choices=["pearson", "spearman", "both"], default="both", help="Correlation method")
    p.set_defaults(func=_dispatch("jsrc.math.correlation"))

    p = math_sub.add_parser("regression", help="Linear or polynomial regression")
    _add_io_args(p)
    p.add_argument("-c", "--col", nargs=2, required=True, metavar=("X", "Y"), help="X and Y column names or indices")
    p.add_argument("-d", "--degree", type=int, default=1, help="Polynomial degree (default: 1)")
    p.set_defaults(func=_dispatch("jsrc.math.regression"))

    p = math_sub.add_parser("ttest", help="Student's t-tests")
    _add_io_args(p)
    p.add_argument("-c", "--col", nargs="+", required=True, help="One (vs mu) or two columns")
    p.add_argument("--mu", type=float, default=0, help="Population mean for one-sample test")
    p.add_argument("--paired", action="store_true", help="Paired t-test (two columns)")
    p.add_argument("--equal-var", action="store_true", help="Assume equal variance (Welch otherwise)")
    p.set_defaults(func=_dispatch("jsrc.math.ttest"))

    p = math_sub.add_parser("chisquare", help="Chi-squared test for independence or goodness-of-fit")
    p.add_argument("-i", "--input", help="Input file (TSV) for contingency table test")
    p.add_argument("-o", dest="output", help="Output file (default: stdout)")
