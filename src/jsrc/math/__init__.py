    p.set_defaults(func=_dispatch("jsrc.math.kmeans"))

    p = math_sub.add_parser("hcluster", help="Hierarchical clustering")
    _add_io_args(p)
    p.add_argument("-m", "--method", choices=["single", "complete", "average"], default="average", help="Linkage method")
