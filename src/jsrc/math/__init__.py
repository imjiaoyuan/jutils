    p.add_argument("--observed", nargs="+", type=float, help="Observed counts for goodness-of-fit")
    p.add_argument("--expected", nargs="+", type=float, help="Expected counts for goodness-of-fit")
    p.add_argument("-c", "--col", nargs=2, metavar=("COL1", "COL2"), help="Two columns for contingency table")
