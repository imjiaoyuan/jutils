    p.add_argument("--paired", action="store_true", help="Wilcoxon signed-rank (paired)")
    p.set_defaults(func=_dispatch("jsrc.math.nonparametric"))

    p = math_sub.add_parser("distribution", help="PDF / CDF queries for standard distributions")
    p.add_argument("-o", dest="output", help="Output file (default: stdout)")
    p.add_argument("--dist", required=True, choices=["normal", "t", "f", "chi2"], help="Distribution name")
